function gm_docs2_pull(str) {
    str = str || undefined;
    
    if (str == undefined) str = "";
    
    if (str === "" || str.replace(/ /g, "") == "") str = Object.keys(gmD)[Math.floor(Math.random()*Object.keys(gmD).length)];
    
    save_str = str;

    str = str.toLowerCase().replace(/[^a-zA-Z]+/g, "");

    let u = 'https://www.google.com/search?q=site%3Adocs2.yoyogames.com+%22' + save_str + '%22';
    
    if (gmD[str] !== undefined) {

        let p = gmD[str];
        
        for (var k in gmG) {
            let re = new RegExp("\\" + k, "g");
            p = p.replace(re, gmG[k]);      
        }

        u = 'https://docs2.yoyogames.com/' + p + '.html';
    }

    return u;
}
